#Using parentheses ()
colors = ("red", "green", "blue")
print("Tuple using ():", colors)

#Using tuple() constructor
nums = tuple([1, 2, 3])
print("Tuple using tuple():", nums)

#uple with one item
single = ("apple",)
print("Single-item tuple:", single)

#Tuple without parentheses
packed = "a", "b", "c"
print("Tuple without ():", packed)

#From a string 
letters = tuple("hello")
print("Tuple from string:", letters)
