#Arithmetic Operators
a, b = 10, 3
print("Add:", a + b)
print("Sub:", a - b)
print("Mul:", a * b)
print("Div:", a / b)
print("Mod:", a % b)

#Relational Operators
print("a > b:", a > b)
print("a == b:", a == b)

#Assignment Operator
x = 5
x += 2
print("x after += 2:", x)

#Logical Operators
print("True and False:", True and False)
print("True or False:", True or False)

#Bitwise Operators
print("a & b:", a & b)
print("a | b:", a | b)

#Ternary Operator
max_val = a if a > b else b
print("Greater value:", max_val)
