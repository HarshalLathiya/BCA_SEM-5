text = "Python"

# i) Indexing
print("Positive Indexing:")
print("text[0]:", text[0])
print("text[3]:", text[3])

print("\nNegative Indexing:")
print("text[-1]:", text[-1])
print("text[-4]:", text[-4])

# ii) Slice Operator
print("\nSlicing:")
print("text[1:4]:", text[1:4])       # from index 1 to 3
print("text[:3]:", text[:3])         # from start to index 2
print("text[2:]:", text[2:])         # from index 2 to end
print("text[::2]:", text[::2])       # every 2nd character
